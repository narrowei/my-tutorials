<h1> My tutorial </h1>
<p1> My-Tutorials is a platform where students could seek for tutorials as well 
as tutors could register tutorials. The website is lightweight, interactive, RESTful, 
could support reasonable size Database. The front-end and back-end are powered by Vue.JS and Node.JS respectively.
</p1>
<h2> Features </h2>
<p2>
User sign-up and log-in/log-out
Different classes of User have different profile page
Students browse through tutorials
Tutors register new tutorials
Students enroll in selected tutorials
Students check out payment
</p2>

<h3> Technologies  </h3>
<p3>
My-Tutorials uses a number of open source projects to work properly:
Vue.js - The Progressive JavaScript Framework
Node.js - Asynchronous event driven JavaScript runtime for the back-end
Express - Fast Node.js web application framework
Webpack - A static module bundler for modern JavaScript applications
Babel - A JavaScript compiler
Element - Vue 2.0 based component library
</p3>


